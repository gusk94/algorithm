def go(x, y, d):
    global res
    if x == N-1 and y == N-1:
        res += 1
        return
    if d == 1 or d == 3:
        if 0 <= y+1 < N:
            if house[x][y+1] == 0:
                go(x, y + 1, 1)
    if d == 2 or d == 3:
        if 0 <= x + 1 < N:
            if house[x+1][y] == 0:
                go(x+1, y, 2)
    if d == 1 or d == 2 or d == 3:
        if 0 <= x+1 < N and 0 <= y+1 < N:
            if house[x+1][y+1] == house[x][y+1] == house[x+1][y] == 0:
                go(x+1, y+1, 3)


N = int(input())
house = [list(map(int, input().split())) for _ in range(N)]
res = 0
go(0, 1, 1)
print(res)


